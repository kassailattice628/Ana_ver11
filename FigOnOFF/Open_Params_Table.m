            'Recording Time(ms)';...
            'Sampling Time(us)';...
            'Stim_Pattern';...
            'Trial#';...
            'Size(deg)';...
            'Position';...
            'Spatial Freq(cycle/deg)';...
            'Temporal Freq(Hz)';...
            'Angle (deg)';
            'Stim_lumi';...
            'BG_lumi';...
            'Stim_Color';...
            'Stim_Duration(sec)';...
            'Corrected Delay(sec)';
            };
    case {'Images'}
        rnames = {
            'Recording Time(ms)';...
            'Sampling Time(us)';...
            'Stim_Pattern';...
            'Trial#';...
            'Size(deg)';...
            'Position';...
            'Image#';...
            'BG_lumi';...
            'Stim_Duration(sec)';...
            'Corrected Delay(sec)';
            };
    case {'Mosaic'}
    case {'FineMap'}
end
%{
rnames = {'Recording Time (ms)';...
    'Sampling Time (us)';...
    'PlotType';'Cycle#';...
    'StimON/OFF';...
