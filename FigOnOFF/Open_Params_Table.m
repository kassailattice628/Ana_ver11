            'Stim_Pattern';...
            'Trial#';...
            'Size(deg)';...
            'Position';...
            'Speed(deg/s)';...
            'Max Size(deg)';...
            'Stim_lumi';...
            'BG_lumi';...
            'Stim_Color';...
            'Stim_Duration(sec)';...
            'Corrected Delay(sec)';
            };
    case {'Sin', 'Rect', 'Gabor'}
